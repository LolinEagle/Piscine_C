int	ft_is_prime(int nb)
{
	int	i;

	if (nb < 2)
		return (0);
	else if (nb == 2)
		return (1);
	i = 1;
	while (i < nb / i)
	{
		i++;
		if (nb % i == 0)
			return (0);
	}
	return (1);
}
