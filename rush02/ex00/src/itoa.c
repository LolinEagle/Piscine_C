#include "header.h"

char	*ft_itoa(int nb)
{
	char	*res;

	res = malloc(4);
	if (!res)
		return (NULL);
	res[0] = nb / 100 + '0';
	res[1] = nb / 10 + '0';
	res[2] = nb % 10 + '0';
	res[3] = '\0';
	return (res);
}
